package bwa_picard_gatk_pipeline.fileWrappers;

/**
 *
 * @author wim
 */
public class CSFastaEntry {
    
    String description;
    String csValue;
    String qualValue;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCsValue() {
        return csValue;
    }

    public void setCsValue(String csValue) {
        this.csValue = csValue;
    }

    public String getQualValue() {
        return qualValue;
    }

    public void setQualValue(String qualValue) {
        this.qualValue = qualValue;
    }
    
    
    
    
           
    
    
    
}
